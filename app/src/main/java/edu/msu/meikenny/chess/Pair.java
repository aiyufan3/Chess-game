package edu.msu.meikenny.chess;

public class Pair {

    private float x_coord;
    private float y_coord;

    public float getY_coord() {
        return y_coord;
    }

    public float getX_coord() {
        return x_coord;
    }

    public Pair(float x, float y) {
        x_coord = x;
        y_coord = y;
    }

}
